# Starting Variables and Data Types in Python

#There are three  types of data in python:

# 1. Numeric data types - int, float, complex numbers
# 2. Non-numeric data types - string, boolean, list, tuple, dictionary
# 3. Special data types - None, set

## Numeric data types ##

num = 14
print(type(num))   # Output : <class 'int'>

float_num = 14.02
print(type(float_num))    # Output : <class 'float'>

complex_num = 5 +  7j   
print(type(complex_num)) # Output : <class 'complex'>


## Non-numeric data types ##

string = 'Hello World'
print(type(string))     # Output : <class 'str'>

bool_val = True
print(type(bool_val))     # Output : <class 'bool'>


